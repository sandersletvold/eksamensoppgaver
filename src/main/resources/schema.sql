CREATE TABLE Pakke (
    PID INTEGER AUTO_INCREMENT NOT NULL,
    LID INTEGER,
    EIER VARCHAR(50),
    VEKT DOUBLE PRECISION,
    VOLUM DOUBLE PRECISION,
    FOREIGN KEY (LID) REFERENCES Lager(LID),
    PRIMARY KEY (PID)
);

CREATE TABLE Lager (
    LID INTEGER AUTO_INCREMENT NOT NULL,
    NAVN VARCHAR(50),
    ADRESSE VARCHAR(50),
    PRIMARY KEY (LID)
);

